	os.Setenv("SLACK_BOT_TOKEN", "xoxb-3854055336275-4147415750791-6wwAaVhr98j8oCVEjCUQUpAd")
	os.Setenv("SLACK_APP_TOKEN", "xapp-1-A044RUDJ398-4164417492516-45dde4dc53e38523775ceabe8b6fe2480a60c449c9a5ae8f34d1f6b106ddb446")

	bot := slacker.NewClient(os.Getenv("SLACK_BOT_TOKEN"), os.Getenv("SLACK_APP_TOKEN"))

	go printCommandEvents(bot.CommandEvents())

	bot.Command("my yob is <year>", &slacker.CommandDefinition{
		Description: "yob calculator",
		Examples:    []string{"my yob is 2020"},
		Handler: func(boxCtx slacker.BotContext, request slacker.Request, response slacker.ResponseWriter) {
			year := request.Param("year")
			yob, err := strconv.Atoi(year)
			if err != nil {
				fmt.Println("Error")
			}
			age := 2022 - yob
			r := fmt.Sprintf("age is %d", age)
			response.Reply(r)
		},
	})

